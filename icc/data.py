def _build_countries():
    """Build the COUNTRIES list by assigning ICC codes from the sequence."""
    countries = []
    for rank, raw in enumerate(_RAW_COUNTRIES, start=1):
        name, official_name, iso2, iso3, iso_num, area, continent, phone = raw
        countries.append({
            "rank": rank,
            "name": name,
            "official_name": official_name,
            "iso_alpha2": iso2,
            "iso_alpha3": iso3,
            "iso_numeric": iso_num,
            "land_area_km2": area,
            "icc_code": ICC_SEQUENCE[rank - 1],
            "continent": continent,
            "phone_code": phone,
        })
    return countries


